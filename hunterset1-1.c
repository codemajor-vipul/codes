#include<stdio.h>
#include<stdlib.h>
void main()
{
	int n,k=0,i,j,count,x,temp;
	scanf("%d",&n);
	int *a=malloc(sizeof(int)*n);
	int *b=malloc(sizeof(int)*n);
	int *c=malloc(sizeof(int)*n);
	for(i=0;i<n;i++)
	{
		scanf("%d",&a[i]);
	}
	for(i=0;i<n;i++)
	{
		c[i]=0;
	}
	for(i=0;i<n;i++)
	{
	    x=a[i];
		count=0;
		for(j=i+1;j<n;j++)
		{
			if(x!=0)
			{
				if(a[j]==x)
				{
					c[i]++;
					a[j]=0;
				}
			}
		}		
	}
	for(i=0;i<n;i++)
	{
		if(c[i]>0)
		{
			b[k]=a[i];
			k++;
		}
	}
	if(k==0)
	{
		printf("unique");
	}
	else
	{
		for(i=0;i<k-1;i++)
		{
			for(j=0;j<k-i-1;j++)
			{
				if(a[j]>a[j+1])
				{
					temp=b[j];
					b[j]=b[j+1];
					b[j+1]=temp;
				}
			}
		}
		for(i=0;i<k;i++)
		{
			printf("%d",b[i]);
		}
	}
}
